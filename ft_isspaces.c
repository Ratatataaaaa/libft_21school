#include "libft.h"

int			ft_isspaces(int c)
{
	if ((char)c == ' ' || (char)c == '\t' || (char)c == '\v' ||
				(char)c == '\f' || (char)c == '\r' || (char)c == '\n')
		return (1);
	else
		return (0);
}
